	protected void addSourceTimeRange(float startTime, float endTime, float speed, boolean flipped) {
		float timeToSpan = P.abs(startTime - endTime);
		timeToSpan /= 4f;
		int framesToAdd = P.round(timeToSpan * (30f/speed) * 2f); // multiply by 2 here to make up for our double array transfer
		P.println("=========== Adding "+framesToAdd+" frames ========");
		for (int i = 0; i < framesToAdd; i++) {
			float spanProgress = (float)i / (float)framesToAdd;
			// adding twice as many frames as we need, then we reduce afterwards
			if(buildPlaybackIndex < frameIndexPlaybackSequenceDouble.length) {
				frameIndexPlaybackSequenceDouble[buildPlaybackIndex] = (int) P.map(spanProgress, 0f, 1f, sourceTimeToFrame(startTime), sourceTimeToFrame(endTime));
				if(flipped == true) frameIndexPlaybackSequenceDouble[buildPlaybackIndex] = -1 * frameIndexPlaybackSequenceDouble[buildPlaybackIndex];
//				P.println(buildPlaybackIndex+" === "+frameIndexPlaybackSequenceDouble[buildPlaybackIndex]);
			} else {
				P.println("Array out of bounds.. "+buildPlaybackIndex);
			}
			buildPlaybackIndex++;
		}
	}
	
	protected int sourceTimeToFrame(float seconds) {
		return P.round(P.map(seconds, 0, 60, 0, 450));
	}
